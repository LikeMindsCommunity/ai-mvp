import 'package:flutter/material.dart';
// Import the LikeMinds Chat SDK core package
import 'package:likeminds_chat_flutter_core/likeminds_chat_flutter_core.dart';

// Modify the main function to initialize the SDK
Future<void> main() async {
  WidgetsFlutterBinding.ensureInitialized(); // Ensure Flutter bindings are initialized
  // Initialize the LikeMinds Chat SDK
  await LMChatCore.instance.initialize();
  // Then run the app
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  // This widget is the root of your application.
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Flutter Demo',
      // Hide debug banner
      debugShowCheckedModeBanner: false,
      theme: ThemeData(
        // This is the theme of your application.
        colorScheme: ColorScheme.fromSeed(seedColor: Colors.deepPurple),
        useMaterial3: true,
      ),
      // Start with the MyHomePage, from where we will trigger chat initiation
      home: const MyHomePage(title: 'Flutter Demo Home Page'),
    );
  }
}

class MyHomePage extends StatefulWidget {
  const MyHomePage({super.key, required this.title});

  final String title;

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  int _counter = 0;

  void _incrementCounter() {
    setState(() {
      _counter++;
    });
  }

  // Function to initiate LikeMinds Chat and navigate
  void _openLikeMindsChat() async {
    // TODO: Replace with your actual API Key
    const String apiKey = "YOUR_API_KEY";
    // TODO: Replace with a unique identifier for your user
    const String uuid = "YOUR_USER_UUID";
    // TODO: Replace with the name to display for the user
    const String userName = "Your User Name";

    // Initiate the user session with LikeMinds Chat using API Key
    LMResponse<void> response = await LMChatCore.instance.showChatWithApiKey(
      apiKey: apiKey,
      uuid: uuid,
      userName: userName,
    );

    if (response.success) {
      // If successful, navigate to the LikeMinds Chat Home Screen
      MaterialPageRoute route = MaterialPageRoute(
        builder: (context) => const LMChatHomeScreen(),
      );
      // Use pushReplacement to replace the current screen with the chat screen
      Navigator.pushReplacement(context, route);
    } else {
      // Handle errors if initiation fails
      debugPrint("Error opening chat: ${response.errorMessage}");
      // Optionally show a SnackBar or Dialog to the user
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Failed to open chat: ${response.errorMessage}')),
      );
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        backgroundColor: Theme.of(context).colorScheme.inversePrimary,
        title: Text(widget.title),
      ),
      body: Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: <Widget>[
            const Text('You have pushed the button this many times:'),
            Text(
              '$_counter',
              style: Theme.of(context).textTheme.headlineMedium,
            ),
            const SizedBox(height: 24), // Add some spacing
            // Add a button to open the LikeMinds Chat
            ElevatedButton(
              onPressed: _openLikeMindsChat,
              child: const Text('Open Chat'),
            ),
          ],
        ),
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _incrementCounter,
        tooltip: 'Increment',
        child: const Icon(Icons.add),
      ),
    );
  }
}
